const heroSlides = document.querySelectorAll(".hero-slide");
const backToTopButton = document.querySelector(".back-to-top");
const portfolioItems = document.querySelectorAll(".portfolio-item");
const modal = document.getElementById("portfolioModal");
const modalImage = document.querySelector(".modal-image");
const modalClose = document.querySelector(".modal-close");
const langButtons = document.querySelectorAll(".lang-btn");
const menuToggle = document.querySelector(".menu-toggle");
const navPanel = document.querySelector(".nav-panel");
const navLinks = document.querySelectorAll(".nav-links a");

let slideIndex = 0;

function rotateSlides() {
  if (!heroSlides.length) return;

  heroSlides.forEach((slide, index) => {
    slide.classList.toggle("active", index === slideIndex);
  });

  slideIndex = (slideIndex + 1) % heroSlides.length;
}

function updateMetadata(lang) {
  const content = translations[lang];
  document.documentElement.lang = lang;
  document.title = content.pageTitle;
  document.querySelector('meta[name="description"]').setAttribute("content", content.metaDescription);
  document.querySelector('meta[property="og:title"]').setAttribute("content", content.ogTitle);
  document.querySelector('meta[property="og:description"]').setAttribute("content", content.ogDescription);
  document.querySelector('meta[name="twitter:title"]').setAttribute("content", content.ogTitle);
  document.querySelector('meta[name="twitter:description"]').setAttribute("content", content.ogDescription);
  document.querySelector('meta[property="og:locale"]').setAttribute("content", lang === "es" ? "es_ES" : "en_US");
}

function applyTranslations(lang) {
  const content = translations[lang];
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (content[key]) {
      element.textContent = content[key];
    }
  });

  langButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  localStorage.setItem("site-language", lang);
  updateMetadata(lang);
}

function openModal(imageSrc, altText) {
  modalImage.src = imageSrc;
  modalImage.alt = altText;
  modal.classList.add("open");
  modal.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeModal() {
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  modalImage.src = "";
  modalImage.alt = "";
  document.body.style.overflow = "";
}

rotateSlides();
setInterval(rotateSlides, 5000);

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyTranslations(button.dataset.lang);
  });
});

portfolioItems.forEach((item) => {
  item.addEventListener("click", () => {
    openModal(item.dataset.image, item.dataset.alt);
  });
});

modalClose.addEventListener("click", closeModal);
modal.addEventListener("click", (event) => {
  if (event.target === modal) closeModal();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && modal.classList.contains("open")) {
    closeModal();
  }
});

window.addEventListener("scroll", () => {
  backToTopButton.classList.toggle("visible", window.scrollY > 500);
});

backToTopButton.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

menuToggle.addEventListener("click", () => {
  const isOpen = navPanel.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
});

navLinks.forEach((link) => {
  link.addEventListener("click", () => {
    navPanel.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  });
});

document.getElementById("currentYear").textContent = new Date().getFullYear();

const initialLanguage = new URLSearchParams(window.location.search).get("lang") || localStorage.getItem("site-language") || "en";
applyTranslations(initialLanguage === "es" ? "es" : "en");
